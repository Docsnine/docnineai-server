/**
 * Get webhook settings for a project.
 * Owner-only. Returns the secret only via this endpoint.
 *
 * @param {{ projectId, userId, apiBaseUrl }}
 * @returns {{ webhookUrl, secret, yaml, lastWebhookAt, lastWebhookStatus, webhookEnabled }}
 */
export async function getWebhookSettings({ projectId, userId, apiBaseUrl }) {
  const project = await assertOwnership(projectId, userId);

  const webhookUrl = `${(apiBaseUrl || "").replace(/\/$/, "")}/api/webhook/${projectId}`;
  const yaml = generateWorkflowYAML({
    apiBaseUrl,
    projectId: projectId,
    repoName: project.repoName,
  });

  return {
    projectId: projectId,
    webhookUrl,
    secret: project.webhookSecret,
    yaml,
    lastWebhookAt: project.lastWebhookAt || null,
    lastWebhookStatus: project.lastWebhookStatus || null,
    webhookEnabled: project.webhookEnabled,
    createdAt: project.createdAt,
  };
}

/**
 * Rotate the webhook secret (regenerate it).
 * Owner-only. Returns the new secret and updated YAML.
 *
 * @param {{ projectId, userId, apiBaseUrl }}
 * @returns {{ webhookUrl, secret, yaml }}
 */
export async function rotateWebhookSecret({ projectId, userId, apiBaseUrl }) {
  const project = await assertOwnership(projectId, userId);

  // Generate new 32-byte hex secret
  const newSecret = randomBytes(32).toString("hex");

  // Update the project
  project.webhookSecret = newSecret;
  await project.save();

  const webhookUrl = `${(apiBaseUrl || "").replace(/\/$/, "")}/api/webhook/${projectId}`;
  const yaml = generateWorkflowYAML({
    apiBaseUrl,
    projectId: projectId,
    repoName: project.repoName,
  });

  return {
    projectId: projectId,
    webhookUrl,
    secret: newSecret,
    yaml,
    message:
      "Secret rotated. Update your GitHub secret to continue using webhooks.",
  };
}

/**
 * Update webhook enabled/disabled status.
 * Owner-only.
 *
 * @param {{ projectId, userId, webhookEnabled }}
 * @returns {{ webhookEnabled }}
 */
export async function updateWebhookSettings({
  projectId,
  userId,
  webhookEnabled,
}) {
  const project = await assertOwnership(projectId, userId);

  if (typeof webhookEnabled !== "boolean") {
    throw domainError(
      "webhookEnabled must be a boolean",
      "INVALID_PARAMETER",
      400,
    );
  }

  project.webhookEnabled = webhookEnabled;
  await project.save();

  return {
    projectId: projectId,
    webhookEnabled: project.webhookEnabled,
  };
}

/**
 * Internal: Update webhook delivery status after a push event.
 * Called from the per-project webhook handler.
 * Non-critical — swallow errors.
 *
 * @param {{ projectId, status: "success" | "failed" | "skipped" }}
 */
export async function updateWebhookStatus({ projectId, status }) {
  try {
    await Project.findByIdAndUpdate(projectId, {
      $set: {
        lastWebhookAt: new Date(),
        lastWebhookStatus: status,
      },
    });
  } catch (err) {
    // Non-critical update — log but don't throw
    console.error(
      `[webhook] Failed to update status for project ${projectId}:`,
      err.message,
    );
  }
}
