// ─── Helper: Create domain error ───────────────────────────

function domainError(msg, code, status = 400) {
  const e = new Error(msg);
  e.code = code;
  e.status = status;
  return e;
}

// ─── Helper: Assert ownership ─────────────────────────────

async function assertOwnership(projectId, userId) {
  const project = await Project.findOne({ _id: projectId, userId });
  if (!project)
    throw domainError("Project not found.", "PROJECT_NOT_FOUND", 404);
  return project;
}

// ─── Generate GitHub Actions Workflow YAML ────────────────
// Generates a per-project workflow that:
//   1. Uses the project's specific webhook secret from GitHub Actions secrets
//   2. Sends to the per-project endpoint: /api/webhook/:projectId
//   3. Writes payload to a temp file using Python (no heredoc byte issues)
//   4. Signs with openssl dgst -sha256 -hmac
//   5. Sends with curl --data-binary @file

export function generateWorkflowYAML({ apiBaseUrl, projectId, repoName }) {
  const base = (apiBaseUrl || "https://your-docnine-instance.com").replace(
    /\/$/,
    "",
  );

  return `# .github/workflows/docnine.yml
# Auto-generated by Docnine
# Triggers an incremental documentation sync on every push to main.
#
# SETUP:
#   1. Go to your repo Settings → Secrets and variables → Actions
#   2. Add a secret named DOCNINE_WEBHOOK_SECRET with the value from:
#      https://app.docnine.ai/projects/${projectId}/webhook
#   3. Commit this file to your repository
#
# The workflow will automatically trigger on push and send a webhook
# to Docnine with the code changes. Only changed files are re-documented.

name: Docnine Auto-Document

on:
  push:
    branches: [ main, master ]
    paths:
      - '**.js'
      - '**.ts'
      - '**.tsx'
      - '**.jsx'
      - '**.py'
      - '**.go'
      - '**.rs'
      - '**.java'
      - '**.kt'
      - '**.prisma'
      - '**.graphql'
      - '**.sql'
      - 'package.json'
      - 'package-lock.json'
      - 'yarn.lock'
      - 'pnpm-lock.yaml'
      - 'requirements.txt'
      - 'go.mod'
      - 'Cargo.toml'
      - 'pom.xml'
      - 'composer.json'
  workflow_dispatch:

jobs:
  docnine-sync:
    name: Docnine Sync
    runs-on: ubuntu-latest
    timeout-minutes: 15

    steps:
      - name: Checkout repository
        uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v4
        with:
          python-version: '3.11'

      - name: Trigger Docnine Webhook
        env:
          WEBHOOK_SECRET: \${{ secrets.DOCNINE_WEBHOOK_SECRET }}
          PROJECT_ID: ${projectId}
        run: |
          # Build the push payload that mimics GitHub's format
          PAYLOAD=\$(python3 << 'PYTHON_EOF'
import json
import os
import sys

payload = {
    "ref": "\${{ github.ref }}",
    "after": "\${{ github.sha }}",
    "pusher": {
        "name": "\${{ github.actor }}"
    },
    "repository": {
        "html_url": "\${{ github.server_url }}/\${{ github.repository }}",
        "full_name": "\${{ github.repository }}",
        "default_branch": "\${{ github.event.repository.default_branch || 'main' }}"
    },
    "commits": \${{ toJson(github.event.commits) }}
}

print(json.dumps(payload, separators=(',', ':')))
PYTHON_EOF
          )

          # Write payload to a temp file (byte-perfect, no processing)
          echo -n "\${PAYLOAD}" > /tmp/webhook_payload.json

          # Compute HMAC-SHA256 signature
          SIGNATURE="sha256=\$(openssl dgst -sha256 -hmac "\${WEBHOOK_SECRET}" /tmp/webhook_payload.json | awk '{print \$2}')"

          echo "Sending webhook to ${base}/api/webhook/\${PROJECT_ID}"

          # Send with curl --data-binary for byte-perfect delivery
          HTTP_STATUS=\$(curl -s -o /tmp/webhook_response.json -w "%{http_code}" \\
            -X POST \\
            -H "Content-Type: application/json" \\
            -H "X-Hub-Signature-256: \${SIGNATURE}" \\
            -H "X-GitHub-Event: push" \\
            --data-binary @/tmp/webhook_payload.json \\
            "${base}/api/webhook/\${PROJECT_ID}")

          RESPONSE=\$(cat /tmp/webhook_response.json)
          echo "HTTP Status: \${HTTP_STATUS}"
          echo "Response: \${RESPONSE}"

          # Fail the step if the server returned an error
          if [ "\${HTTP_STATUS}" -ge 400 ]; then
            echo "❌ Webhook returned HTTP \${HTTP_STATUS}"
            echo "Error: \$(echo "\${RESPONSE}" | jq -r '.error // .message // "Unknown error"')"
            exit 1
          fi

          echo "✓ Webhook delivered successfully"
`;
}

